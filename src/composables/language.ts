// 获取语言显示名称
export function getLanguageDisplayName(code: string): string {
  const lang = languages.find(l => l.code === code)
  if (!lang)
    return code

  if (lang.name === lang.nativeName) {
    return `${lang.flag} ${lang.name}`
  }
  return `${lang.flag} ${lang.name} (${lang.nativeName})`
}
